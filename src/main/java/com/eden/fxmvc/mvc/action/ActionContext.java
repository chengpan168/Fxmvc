package com.eden.fxmvc.mvc.action;

/**
 * action context contain action name and message of page
 * @author eden
 *
 */
public class ActionContext {
	private String path ;
	private ActionMessage actionMessage ;
	
	
	public ActionContext(String path) {
		this.path = path ;
	}
	public ActionContext(String action , ActionMessage actionMessage ) {
		this.path = action ; 
		this.actionMessage = actionMessage ;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public ActionMessage getActionMessage() {
		return actionMessage;
	}
	public void setActionMessage(ActionMessage actionMessage) {
		this.actionMessage = actionMessage;
	}
	
	
}
